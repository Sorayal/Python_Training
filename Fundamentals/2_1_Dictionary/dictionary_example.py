# Dictionary
# used with {} to define
# key/value
persons = {"name": "Nick", "age": 27, "hobby": "coding"}
print(persons.keys())

# Calling for key 'hobby'
print(persons.get("hobby"))

# Prints the values in the dictionary
print(persons.values())

# Prints the count of elements in the dictionary
print(len(persons))

# Calling value for age
print(persons["age"])
