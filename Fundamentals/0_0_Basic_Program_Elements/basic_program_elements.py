# Sequence
# Sequences will be executed from the top to the bottom simply
print("--*Sequence*--")
print("Sequence Step 1")
print("Sequence Step 2")
print("Sequence Step 3")

# Conditional 
# Depending on the condition the program executes different paths 
print("-*Conditional*--")
firstNumber = 50
if firstNumber > 50:
    print("Number is over 50")
elif firstNumber < 50:
    print("Number is below 50")
else:
    print("Number is 50")

# Repeat / Loops
# loops will be executed until the exit conditon is reached
print("--*Repeat/Loops*--")
count = 10 
while count > 0:
    print(count)
    count = count - 1
