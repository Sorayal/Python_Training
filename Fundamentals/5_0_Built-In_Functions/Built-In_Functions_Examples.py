# Converting to string
str(5)
str(5.6)
str(True)

int("5")
float("5.6")
bool("True")

# Determine the length of strings or lists
len("Hello")
len([2, 3, 4, 6, 7])
len(["apple", "banana"])

# sort
sorted_array = sorted([22, 3, 45, 6, 7, 0, 55, 156])
print(sorted_array)

# Capitalize chars will be put to the beginning
sorted_array = sorted(["B", "R", "N", "a"])
print(sorted_array)

# priority sort, int -> float -> big letters -> small letters
sorted_array = sorted(["B", "2", "5.6", "R", "7.7", "1", "N", "a"])
print(sorted_array)
