# A week
# Write the code that will print all days of the week on separate lines with an empty line between them. Make sure that each day starts with a capital letter. Start it with Sunday.
#
#
# Sample Input:
#
# Sample Output:
# Sunday
#
# Monday
#
# Tuesday
#
# Wednesday
#
# Thursday
#
# Friday
#
# Saturday


week = ["Sunday", "Monday", "Tuesday", "Wednesday", "Thursday", "Friday", "Saturday"]
for day in week:
    print(day)
    print("")
